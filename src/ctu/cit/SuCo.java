package ctu.cit;

public class SuCo {
	private String SC_MA;
	private String SC_TEN;
	
	//Get
	public String getMaSuCo (String maSuCo)
	{
		return SC_MA;
	}
	public String getTenSuCo (String tenSuCo)
	{
		return SC_TEN;
	}
	//Set
	public void setMaSuCo (String maSuCo)
	{
		this.SC_MA = maSuCo;
	}
	public void setTenSuCo (String tenSuCo)
	{
		this.SC_TEN = tenSuCo;
	}

}
